for num in range(5): # 0,1,2,3,4
    print(num)

for num in range(2,5): # 2,3,4
    print(num)


for num in range(2,10,2): # 2,4,6,8 , cuenta de 2 en 2 hasta (10 - 1), por eso se queda en el 8
    print(num)

lista_numeros = list(range(5)) # crea una lista con los numeros del rango
print(lista_numeros)

